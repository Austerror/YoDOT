local keywordHandler = KeywordHandler:new()
local npcHandler = NpcHandler:new(keywordHandler)
NpcSystem.parseParameters(npcHandler)

-- OTServ event handling functions start
function onCreatureAppear(cid) npcHandler:onCreatureAppear(cid) end
function onCreatureDisappear(cid) npcHandler:onCreatureDisappear(cid) end
function onCreatureSay(cid, type, msg) npcHandler:onCreatureSay(cid, type, msg) end
function onThink() npcHandler:onThink() end
-- OTServ event handling functions end
function creatureSayCallback(cid, type, msg)
	if(not npcHandler:isFocused(cid)) then
		return FALSE
	end
	
	local obHandIns = getPlayerStorageValue(cid,2315)
	local swordAddon = 2488
	if msgcontains(msg, 'addon') then
		if getPlayerStorageValue(cid, swordAddon) == -1 then
			npcHandler:say('Bring me 100 pieces of iron ore and a piece of royal steel then talk to me about the {sword}. The machine on the roof of this building might help you transmute some items.', cid)
		else
			npcHandler:say('You have earned your warrior sword addon.', cid)
		end
	elseif msgcontains(msg, 'sword') then
		if getPlayerStorageValue(cid, swordAddon) == -1 then
			if getPlayerItemCount(cid, 5880) >= 100 and getPlayerItemCount(cid, 5887) >= 1 then
				doPlayerRemoveItem(cid, 5880, 100)
				doPlayerRemoveItem(cid, 5887, 1)
				if getPlayerSex(cid) == 0 then
					doPlayerAddOutfit(cid,142,2) -- f
				else
					doPlayerAddOutfit(cid,134,2) -- m
				end
				doSendMagicEffect(getCreaturePosition(cid), 12)
				npcHandler:say('Congratulations! You have received the warrior sword addon.', cid)
			else
				npcHandler:say('Bring me 100 pieces of iron ore and a piece of royal steel then talk to me about the {sword}. The machine on the roof of this building might help you transmute some items.', cid)
			end
		else
			npcHandler:say('You have earned your warrior sword addon.', cid)
		end
	elseif msgcontains(msg,'obsidian knife') and obHandIns == -1 then
		npcHandler:say('If you bring me 5 obsidian lances I will craft you an obsidian knife out of them. You don\'t have to bring me all 5 at once either. Ask me about it again if you are interested.', cid)
		setPlayerStorageValue(cid,2315,1)
	elseif msgcontains(msg,'obsidian knife') and obHandIns == 1 then
		npcHandler:say('Would you like to hand in your first obsidian lance?', cid)
		setPlayerStorageValue(cid,2315,2)
	elseif msgcontains(msg,'yes') and obHandIns == 2 then
		if getPlayerItemCount(cid,2425) >= 1 then
			doPlayerRemoveItem(cid,2425,1)
			setPlayerStorageValue(cid,2315,3)
			npcHandler:say('Thank you, I only need 4 more obsidian lances to craft your new obsidian knife.', cid)
		else
			npcHandler:say('I\'m sorry but you don\'t have one.', cid)
			setPlayerStorageValue(cid,2315,-1)
		end
	elseif msgcontains(msg,'obsidian knife') and obHandIns == 3 then
		npcHandler:say('Would you like to hand in your second obsidian lance?', cid)
		setPlayerStorageValue(cid,2315,4)
	elseif msgcontains(msg,'yes') and obHandIns == 4 then
		if getPlayerItemCount(cid,2425) >= 1 then
			doPlayerRemoveItem(cid,2425,1)
			setPlayerStorageValue(cid,2315,5)
			npcHandler:say('Thank you, I only need 3 more obsidian lances to craft your new obsidian knife.', cid)
		else
			npcHandler:say('I\'m sorry but you don\'t have one.', cid)
			setPlayerStorageValue(cid,2315,3)
		end
	elseif msgcontains(msg,'obsidian knife') and obHandIns == 5 then
		npcHandler:say('Would you like to hand in your third obsidian lance?', cid)
		setPlayerStorageValue(cid,2315,6)
	elseif msgcontains(msg,'yes') and obHandIns == 6 then
		if getPlayerItemCount(cid,2425) >= 1 then
			doPlayerRemoveItem(cid,2425,1)
			setPlayerStorageValue(cid,2315,7)
			npcHandler:say('Thank you, I only need 2 more obsidian lances to craft your new obsidian knife.', cid)
		else
			npcHandler:say('I\'m sorry but you don\'t have one.', cid)
			setPlayerStorageValue(cid,2315,5)
		end
	elseif msgcontains(msg,'obsidian knife') and obHandIns == 7 then
		npcHandler:say('Would you like to hand in your fourth obsidian lance?', cid)
		setPlayerStorageValue(cid,2315,8)
	elseif msgcontains(msg,'yes') and obHandIns == 8 then
		if getPlayerItemCount(cid,2425) >= 1 then
			doPlayerRemoveItem(cid,2425,1)
			setPlayerStorageValue(cid,2315,9)
			npcHandler:say('Thank you, I only need 1 more obsidian lance to craft your new obsidian knife.', cid)
		else
			npcHandler:say('I\'m sorry but you don\'t have one.', cid)
			setPlayerStorageValue(cid,2315,7)
		end
	elseif msgcontains(msg,'obsidian knife') and obHandIns == 9 then
		npcHandler:say('Would you like to hand in your last obsidian lance?', cid)
		setPlayerStorageValue(cid,2315,10)
	elseif msgcontains(msg,'yes') and obHandIns == 10 then
		if getPlayerItemCount(cid,2425) >= 1 then
			doPlayerRemoveItem(cid,2425,1)
			doPlayerAddItem(cid, 5908, 1)
			setPlayerStorageValue(cid,2315,-1)
			npcHandler:say('Excellent, one moment... There you go, a \'new\' obsidian knife.', cid)
		else
			npcHandler:say('I\'m sorry but you don\'t have one.', cid)
			setPlayerStorageValue(cid,2315,9)
		end
	end
end


